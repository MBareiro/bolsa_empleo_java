    private void jButton_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_guardarActionPerformed
        int validacion = 0, nums = 0;
        int fechas = 0;
        String empresa, puesto, desde, hasta;

        empresa = txt_empresa.getText().trim();
        puesto = txt_puesto.getText().trim();
        desde = ((JTextField) txt_desde.getDateEditor().getUiComponent()).getText();
        hasta = ((JTextField) txt_hasta.getDateEditor().getUiComponent()).getText();

        if (txt_desde.getDate() == null) {
            txt_desde.setBackground(Color.red);
            validacion++;
        } else {
            txt_desde.setBackground(Color.white);
        }

        if (txt_hasta.getDate() == null) {
            txt_hasta.setBackground(Color.red);
            validacion++;
        } else {
            txt_hasta.setBackground(Color.white);
        }

        if (empresa.equals("")) {
            txt_empresa.setBackground(Color.red);
            validacion++;
        } else {
            txt_empresa.setBackground(Color.white);
        }
        if(empresa.matches("[0-9]*")){
            nums++;
        }        

        if (puesto.equals("")) {
            txt_puesto.setBackground(Color.red);
            validacion++;
        } else {
            txt_puesto.setBackground(Color.white);
        }
        if(puesto.matches("[0-9]*")){
            nums++;
        }

        String desde1 = new SimpleDateFormat("yyyy/MM/dd").format(this.txt_desde.getDate());
        String hasta1 = new SimpleDateFormat("yyyy/MM/dd").format(this.txt_hasta.getDate());

        SimpleDateFormat formato = new SimpleDateFormat("yyyy/MM/dd");

        try {
            System.out.println("Entro");
            Date f1 = formato.parse(desde1);
            Date f2 = formato.parse(hasta1);

            if (f1.after(f2)) {
                txt_desde.setBackground(Color.red);
                fechas++;
            } else {
                txt_hasta.setBackground(Color.white);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }

        if (validacion == 0 && fechas == 0 && nums == 0) {
            try {
                Connection cn2 = Conexion.conectar();

                PreparedStatement pst2 = cn2.prepareStatement(
                        "UPDATE experiencias SET empresa = ?, puesto = ?, desde = ?, hasta = ? WHERE id = '" + GestionarExperiencias.id_exp + "'");
                pst2.setString(1, empresa);
                pst2.setString(2, puesto);
                pst2.setString(3, desde);
                pst2.setString(4, hasta);

                pst2.executeUpdate();
                cn2.close();
                JOptionPane.showMessageDialog(null, "Guardado correcto!");
            } catch (SQLException e) {
                System.err.println("Error al actualizar " + e);
            }
        } else if (validacion > 0) {
            JOptionPane.showMessageDialog(null, "Debes llenar todos los campos");
        } else if (nums > 0) {
            JOptionPane.showMessageDialog(null, "Empresa y puesto solo pueden contener letras");
        } else {
            JOptionPane.showMessageDialog(null, "Desde no puede ser posterior a Hasta");
        }

    }//GEN-LAST:event_jButton_guardarActionPerformed

