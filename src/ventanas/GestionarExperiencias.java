    public void obtenerDatosTabla() {
        jTable_usuarios.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                int fila_point = jTable_usuarios.rowAtPoint(e.getPoint());
                int columna_point = 0;

                if (fila_point > -1) {
                    id_exp = (int) model.getValueAt(fila_point, columna_point);
                    InformacionExperiencia cargar_exp;
                    
                    try {
                        cargar_exp = new InformacionExperiencia();
                        cargar_exp.setVisible(true);
                        dispose();
                    } catch (ParseException ex) {
                        Logger.getLogger(GestionarExperiencias.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    
                }
            }
        });
    }
